    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        switch(btnModificar.getText()){
            case "Modificar":
                int fil_sel = jTable1.getSelectedRow();
                try {
                    String url, usuario, psw;
                    if(fil_sel == -1){
                        JOptionPane.showMessageDialog(this, "Seleccione el usuario a modificar");
                    }else{
                        //se recuperan los valores de la tabla luego de seleccionarlos
                        m1 = (DefaultTableModel) jTable1.getModel();
                        codigo = (jTable1.getValueAt(fil_sel, 0).toString());
                        url = (jTable1.getValueAt(fil_sel, 1).toString());
                        usuario = (jTable1.getValueAt(fil_sel, 2).toString());
                        psw = (jTable1.getValueAt(fil_sel, 3).toString());
                        //luego de tenerlos se los mandamos a los txt y el id se queda ahi donde esta
                        txtUrl.setText(url);
                        txtUrl.requestFocus();
                        txtUsuario.setText(usuario);
                        txtPsw.setText(psw);
                        nuevo_mod();
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
                break;
            case "Guardar":
                try {
                    Usuario user = new Usuario();
                    user.setId(Integer.parseInt(codigo));
                    user.setUrl(txtUrl.getText());
                    user.setUsuario(txtUsuario.getText());
                    user.setPsw(txtPsw.getText());
                    if(con.modificar(user)){
                        JOptionPane.showMessageDialog(this, "Â¡Usuario modificado con exito!");
                        btnModificar.setText("Modificar");
                        cargarTabla();
                        formato_ancho();
                        cancelarModificar();
                    }else{
                        JOptionPane.showMessageDialog(this, "Â¡Error al modificar el usuario!"); 
                    }

                }catch(Exception e){
                    e.printStackTrace();
                }
                break;
        }
        
        
    }//GEN-LAST:event_btnModificarActionPerformed

