    private void guardar(){
        String usuario, url, psw;
        url = txtUrl.getText();
        usuario = txtUsuario.getText();
        psw = txtPsw.getText();
       
        Usuario usu = new Usuario(url, usuario, psw);
       
        String sql = "INSERT INTO Account (url, usuario, password) VALUES('" + usu.getUrl() + "', '" + usu.getUsuario()+ "' , '" + usu.getPsw() + "')";
        try{
            PreparedStatement pstm = con.getConnection().prepareStatement(sql);
            pstm.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos guardados con exito");
            cancelarNuevo();
        }catch(SQLException e1){
            e1.printStackTrace();
        }
    }
    
    private void buscar(String sql){
        if(con.getConnection() != null){
            try {
                con.st = con.getConnection().createStatement();
                con.rs = con.st.executeQuery(sql);
                //m1 = (DefaultTableModel) jTable1.getModel();
                m1.setRowCount(0);
                while(con.rs.next()){
                    Object[] fila = {con.rs.getInt(1), con.rs.getString(2), con.rs.getString(3), con.rs.getString(4)};
                    m1.addRow(fila);
                }
                con.rs.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }
    
