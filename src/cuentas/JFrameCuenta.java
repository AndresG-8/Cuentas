    private void cargarTabla(){
        //se verifica que la conexiÃ³n este abierta y se procede con la consulta de datos y cargado de la tabla
        if(con.getConnection() != null){
            try{
                String sql = "select * from Account";
                //con.st = con.conn.createStatement();
                con.st = con.getConnection().createStatement();
                con.rs = con.st.executeQuery(sql);
                try{
                    m1 = (DefaultTableModel)jTable1.getModel();
                    m1.setRowCount(0);
                    ResultSetMetaData metaDatos = con.rs.getMetaData();//aca van los agregados para consultar los titulos
                    // Se obtiene el numero de columnas.
                    int numeroColumnas = metaDatos.getColumnCount();
                    // Se obtienen las etiquetas para cada columna
                    Object[] etiquetas = new Object[numeroColumnas];
                    for (int i = 0; i < numeroColumnas; i++){
                             etiquetas[i] = metaDatos.getColumnLabel(i + 1);
                    }
                    // Se meten las etiquetas en el modelo. El numero
                    // de columnas se ajusta automÃ¡ticamente.
                    m1.setColumnIdentifiers(etiquetas);
                //se recorren los datos del result set y se organizan en un aray de objetos para luego pasarlo a la tabla
                while(con.rs.next()){
                    Object []fila = {con.rs.getInt(1),con.rs.getString(2),con.rs.getString(3),con.rs.getString(4)};
                    m1.addRow(fila);
                }
                }catch(SQLException ex){
                    JOptionPane.showMessageDialog(this, " problema con default" + ex);
                }
            }catch(SQLException e){
                e.printStackTrace();
            }
        }else{
            JOptionPane.showMessageDialog(this, "La conexion esta cerrada\n" + con);
        }
    }   
      
